num1 = int(input("Print your first number: "))  
num2 = int(input("Print your second number: "))  
operator = input("Enter + for addition, - for subtraction, * for multiplication, / for division: ")  


if operator == "+":
    print(num1 + num2)
elif operator == "-":
    print(num1 - num2)
elif operator == "*":
    print(num1 * num2)
elif operator == "/":  
    print(num1/num2)
